public class Sample007 {

    public static void main(String[] args) {

        // Atama operatörlerinin kullanımı
        int sonuc = 4;

        sonuc += 2; // sonuc = sonuc + 2;
        System.out.println(sonuc);

        sonuc -= 2; // sonuc = sonuc - 2;
        System.out.println(sonuc);

        sonuc *= 2; // sonuc = sonuc * 2;
        System.out.println(sonuc);

        sonuc /= 2; // sonuc = sonuc / 2;
        System.out.println(sonuc);

        sonuc %= 2; // sonuc = sonuc % 2;
        System.out.println(sonuc);

    }
}
